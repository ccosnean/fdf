#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s;

	s = s1;
	while (*s != '\0')
		s++;
	while (n-- && *s2)
	{
		*s = *s2;
		s2++;
		s++;
	}
	*s = '\0';
	return (s1);
}
